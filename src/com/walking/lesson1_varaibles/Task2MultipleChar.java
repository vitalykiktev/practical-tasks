package com.walking.lesson1_varaibles;

/**
 * Используя несколько переменных типа char вывести свое имя в консоль.
 */
public class Task2MultipleChar {
    public static void main(String[] args) {
        char j = 'J';
        System.out.print(j);

        char a = 'a';
        System.out.print(a);

        char c = 'c';
        System.out.print(c);

        char k = 'k';
        System.out.print(k);
    }
}
